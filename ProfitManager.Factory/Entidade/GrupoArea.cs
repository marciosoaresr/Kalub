
namespace ProfitManager.Fabrica.Entidade
{
    public class GrupoArea: EntidadeBase
    {

        public virtual string Codigo { get; set; }
        public virtual string Nome { get; set; }


        public virtual string NomeNormalizado
        {
            get
            {
                return Codigo + " - " + Nome;
            }
        }

    }
}
